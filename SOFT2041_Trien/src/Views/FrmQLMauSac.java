    private void showData(List<QLMauSac> listMauSac) {
        dtm.setRowCount(0);
        for (QLMauSac qLMauSac : listMauSac) {
            dtm.addRow(qLMauSac.toDataRow());
        }
    }

    private void fillData(int index) {
        QLMauSac p = listQlMauSac.get(index);
        txtMa.setText(p.getMa());
        txtTen.setText(p.getTen());
    }

    private void insert() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        if (ma.length() == 0 || ten.length() == 0) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong");
        } else {
            QLMauSac qLMauSac = new QLMauSac(ma, ten);
            JOptionPane.showMessageDialog(this, qlmssi.insert(qLMauSac));
            showData(listQlMauSac = qlmssi.getAll());
        }
    }

    private void update() {
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            QLMauSac ql = listQlMauSac.get(row);
            UUID id = ql.getId();
            String ma = txtMa.getText();
            String ten = txtTen.getText();
            if (ma.length() == 0 || ten.length() == 0) {
                JOptionPane.showMessageDialog(this, "Khong duoc de trong");
            } else {
                QLMauSac qLMauSac = new QLMauSac(ma, ten);
                JOptionPane.showMessageDialog(this, qlmssi.update(qLMauSac, id));
                showData(listQlMauSac = qlmssi.getAll());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chon doi tuong de sua");
        }
    }

    private void delete() {
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            QLMauSac ql = listQlMauSac.get(row);
            UUID id = ql.getId();
            JOptionPane.showMessageDialog(this, qlmssi.delete(id));
            showData(listQlMauSac = qlmssi.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Chon doi tuong de sua");
        }
    }

